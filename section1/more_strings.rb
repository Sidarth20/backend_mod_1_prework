# variable declaring the types of people
types_of_people = 10
# variable declaring the types of people with it's value
x = "There are #{types_of_people} types of people."
# variable declared with a string
binary = "binary"
# variable declared with a string
do_not = "don't"
# variable declaring that some people know binary and others don't
y = "Those who know #{binary} and those who #{do_not}."

# prints the variable x
puts x
# prints the variable y
puts y

# prints the string with the value of the variable x
puts "I said: #{x}."
# prints the string with the value of the variable y
puts "I also said: '#{y}'."

# variable declared with a boolean
hilarious = false
# variable that evaluates a joke with a string & another variable
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# prints the joke evaluation variable
puts joke_evaluation

# string assigned to variable w
w = "This is the left side of..."
# string assigned to variable e
e = "a string with a right side."

# prints the variables w and e together
puts w + e

